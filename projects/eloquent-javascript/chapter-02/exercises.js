function fizzBuzz() {
  for(let i = 1; i <= 15; i++){
    if(i % 3 !== 0 && i % 5 !== 0){
      console.log(i);
    }else if (i % 3 === 0 && i % 5 === 0){
      console.log("fizzbuzz");
    }else if (i % 3 === 0){
      console.log("fizz");
    }else{
      console.log("buzz");
    }
  }
}

