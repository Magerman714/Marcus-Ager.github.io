function drawChessboard(num=8) {
  let pos1 = "";
  let pos2 = "";
  for(let i = 0; i < num; i++){
    if(i % 2 !== 0){
      pos1 += " ";
      pos2 += "#";
    }else{
      pos1 += "#";
      pos2 += " ";
    }
  }
  pos1 = pos1 + "\n";
  pos2 = pos2 + "\n";
  for(let i = 0; i < num; i++){
    if(i % 2 !== 0){
      console.log(pos1);
    }else{
      console.log(pos2);
    }
}
}

