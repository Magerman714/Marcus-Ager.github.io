////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(x, y) {
  if(x > y){
    return y;
  }else{
    return x;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  if(num === 0){
    return true;
  }else if(num === 1){
    return false;
  }
  if(num > 0){
    return isEven(num - 2);
  }else if(num < 0){
    return isEven(num + 2);
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
  let count = 0;
  for(let i = 0; i < str.length; i++){
    if(str.charAt(i) === char){
      count++;
    }
  }
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
  let count = 0;
  for(let i = 0; i < str.length; i++){
    if(str.charAt(i) === "B"){
      count++;
    }
  }
  return count;
}

